#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main()
{
    int a,b;
    scanf("%d",&a);
    scanf("%d",&b);
    printf("%d ",a+b);
    printf("%d\n",a-b);
    float c,d;
    scanf("%f",&c);
    scanf("%f",&d);
    printf("%0.1f ",c+d);
    printf("%0.1f",c-d);
    return 0;
}
